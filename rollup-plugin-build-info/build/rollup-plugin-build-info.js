// rollup-plugin-build-info.ts
import { exec } from "child_process";
var MODULE_NAME = "@moreplease/build-info";
var LOAD_NAME = "\0@moreplease/build-info";
async function branch() {
  if (process.env.GITHUB_REF_NAME) {
    return process.env.GITHUB_REF_NAME;
  }
  return new Promise((resolve, reject) => {
    exec("git rev-parse --abbrev-ref HEAD", {}, (error, stdout, stderr) => {
      if (stderr) {
        console.warn(stderr);
      }
      if (error) {
        reject(error);
      }
      resolve(stdout.trim());
    });
  });
}
async function commit() {
  if (process.env.GITHUB_SHA) {
    return process.env.GITHUB_SHA;
  }
  return new Promise((resolve, reject) => {
    exec(`git show --format="%H" -s HEAD`, {}, (error, stdout, stderr) => {
      if (stderr) {
        console.warn(stderr);
      }
      if (error) {
        reject(error);
      }
      const commit2 = stdout.trim();
      resolve(commit2);
    });
  });
}
async function buildInfo() {
  return {
    timestamp: /* @__PURE__ */ new Date(),
    branch: await branch(),
    commit: await commit()
  };
}
function plugin() {
  let info = buildInfo();
  return {
    name: "rollup-plugin-build-info",
    buildStart() {
      info = buildInfo();
    },
    resolveId(source) {
      return source === MODULE_NAME ? LOAD_NAME : null;
    },
    async load(id) {
      if (id !== LOAD_NAME) {
        return null;
      }
      const { timestamp, branch: branch2, commit: commit2 } = await info;
      const code = [
        `// Generated by rollup-plugin-build-info`,
        ``,
        `export const timestamp = new Date("${timestamp}");`,
        `export const branch = "${branch2}";`,
        `export const commit = ${JSON.stringify(commit2, null, 2)};`,
        ``,
        `export default { timestamp, branch, commit };`,
        ``
      ].join("\n");
      return code;
    }
  };
}
export {
  branch,
  buildInfo,
  commit,
  plugin as default
};
